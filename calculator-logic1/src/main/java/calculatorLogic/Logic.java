package calculatorLogic;

public class Logic {
    public double osszeadas(double a, double b) {
        return a + b;
    }//end osszeadas

    public double kivonas(double a, double b) {
        return a - b;
    }//end kivonas

    public double szorzas(double a, double b) {
        return a * b;
    }//end szorzas

    public double osztas(double a, double b) {
        return a / b;
    }//end osztas
}
