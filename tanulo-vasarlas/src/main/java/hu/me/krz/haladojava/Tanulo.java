package hu.me.krz.haladojava;

public class Tanulo {
	private final String nev;
	private int penz;
	
	public Tanulo(String nev, int penz) {
		super();
		this.nev = nev;
		this.penz = penz;
	}
	
	private String getNev() {
		return nev;
	}

	private int getPenz() {
		return penz;
	}

	
	
	
}
